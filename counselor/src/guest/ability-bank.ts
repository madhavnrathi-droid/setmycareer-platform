// The digital Ability battery — the founder's real DBDA (David's Battery of
// Differential Abilities, Loratis) subtests, digitised for the /t/ flow.
//
// Seven subtests, at the official DBDA durations and item counts (from the Test
// Administrator instructions): CA 20 · VA 24 (15 synonyms + 9 proverbs) · NA 20 ·
// RA 12 · SA 72 · MA 25 · CL 72. The two speeded subtests (SA, CL) hide their
// timer, exactly like the paper form ("*do not disclose the duration"). Raw
// scores map onto the Loratis norm tables (src/guest/ability-norms.ts).
//
// Verbal items, the reasoning letter-sets and the clerical pairs are the real
// booklet items; their answer keys are derived deterministically (a clerical pair
// is "same" iff the two strings are equal; a closure item's correct option is the
// one true anagram of the target word). Spatial figures are generated by
// construction (a mirror can never equal a rotation, so the key can't disagree
// with the stimulus), and the closure options are generated so exactly one is a
// true anagram — both are correct-by-construction, never a transcription guess.

import type { AbilityKey } from "./ability-norms"

export type SectionKey = AbilityKey

export interface McqItem {
  text: string
  options: string[]
  /** index into options */
  answer: number
  passage?: string
  /** closure (CA): the target word shown degraded above the jumbled options */
  word?: string
}

export interface SpatialTrial { shape: number; rot: 90 | 180 | 270; mirrored: boolean }
export interface ClericalPair { left: string; right: string; same: boolean }

export interface AbilitySection {
  key: SectionKey
  label: string
  minutes: number
  kind: "mcq" | "closure" | "spatial" | "clerical"
  normed: boolean
  /** speeded subtests hide the countdown (paper form: "do not disclose duration") */
  hideTimer?: boolean
  /** one-line "what this measures" for the section intro */
  measures: string
  /** how to answer, taught before the clock starts */
  how: string
  items?: McqItem[]
  spatial?: SpatialTrial[]
  clerical?: ClericalPair[]
}

