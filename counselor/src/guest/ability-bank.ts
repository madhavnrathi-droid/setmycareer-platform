// ── Closure Ability (CA) — 20 items · 5 min ──────────────────────────────────
// DBDA format: a word appears degraded on the left; among five jumbled letter
// strings you pick the ONE that spells the same word (a true anagram of it). The
// correct option is a scramble of the target; the four distractors are scrambles
// with one letter changed, so exactly one option is a real anagram — the key is
// correct by construction, not read off the booklet.
function seededShuffle<T>(arr: T[], seed: number): T[] {
  const a = arr.slice()
  let s = seed >>> 0
  for (let i = a.length - 1; i > 0; i--) {
    s = (s * 1664525 + 1013904223) >>> 0
    const j = s % (i + 1)
    ;[a[i], a[j]] = [a[j], a[i]]
  }
  return a
}
function scrambleWord(word: string, seed: number): string {
  let s = seed, out = seededShuffle(word.split(""), s).join("")
  while (out === word) { s += 101; out = seededShuffle(word.split(""), s).join("") }
  return out
}
function nearMiss(word: string, seed: number): string {
  const chars = word.split("")
  const pos = (seed >>> 0) % chars.length
  const code = chars[pos].charCodeAt(0) - 97
  chars[pos] = String.fromCharCode(97 + ((code + 1 + (seed % 5)) % 26))
  if (chars[pos] === word[pos]) chars[pos] = String.fromCharCode(97 + ((code + 3) % 26))
  return seededShuffle(chars, seed + 17).join("")
}
