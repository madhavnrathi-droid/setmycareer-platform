const CLOSURE: McqItem[] = CA_WORDS.map((word, i) => {
  const correct = scrambleWord(word, i * 31 + 7)
  const distractors = [0, 1, 2, 3].map((k) => nearMiss(word, i * 53 + k * 29 + 11))
  const pos = i % 5
  const options = [...distractors.slice(0, pos), correct, ...distractors.slice(pos)]
  return { word, options, answer: pos, text: "" }
})

